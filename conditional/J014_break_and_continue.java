/*
 <> break stmt:- The break stmt is used to "exit" the loop irrespective of whether the condition is true or false. 
 Whenver a "break" is encountered inside  the loop, the control is sent outside the loop. 

 <> continue stmt:- The continue stmt is used to immediately move to teh next iteration of the loop. The control is taken to the next iteration thus skipping everything below "continue" inside the loop for that iteration. 

 >> In-short, 
 1. "break" stmt completely exits the loop.
 2. "continue" stmt skips the particular iteration of the loop. 
 */