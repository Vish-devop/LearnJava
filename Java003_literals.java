/*
 Literals:- These are the constant value which can be assigned to the variable. 
 e.g. -> char str ="A";
 Literals usually written in single quotes and double quotes. 

 */
public class Java003_literals{
public static void main (String[] args){
    byte age = 45;
    int age2=45;
    short age3=56;
    long ageDino=5444434L;
    char ch='A';
    float f1=5.6f;
    double d1=4.66;
    boolean a=true;

    System.out.print(age);
    String str="James";
    System.out.print(str);
}
}